// 轉換片假名為平假名
function katakanaToHiragana(input) {
    return input.replace(/[\u30A1-\u30F6]/g, function(match) {
        return String.fromCharCode(match.charCodeAt(0) - 0x60);
    });
}

// 轉換羅馬字為平假名
function romajiToHiraganaConversion(input) {
    let result = '';
    let temp = '';

    for (let char of input) {
        temp += char;
        if (romajiToHiragana[temp]) {
            result += romajiToHiragana[temp];
            temp = '';
        } else if (temp.length > 2) {
            result += temp[0];
            temp = temp.slice(1);
        }
    }

    return result + temp;
}

// 統一轉換函數
function convert_jp(input) {
    // 先將片假名轉換為平假名
    let result = katakanaToHiragana(input);
    // 再將羅馬字轉換為平假名
    result = romajiToHiraganaConversion(result.toLowerCase());
    return result;
}

